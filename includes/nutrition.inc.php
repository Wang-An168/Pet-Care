        <h2>Exercise and Activity</h2>
        <p>
            Regular activity keeps pets healthy and prevents obesity and behavioral issues.
            Dogs need daily walks and playtime, cats benefit from interactive toys, and small pets need safe space to move.
        </p>
    </div>

    <div class="block">
        <h2>Grooming and Hygiene</h2>
        <ul>
            <li>Regular brushing to reduce shedding and prevent tangles</li>
            <li>Bathing when necessary using pet-safe products</li>
            <li>Nail trimming to prevent discomfort</li>
            <li>Ear and dental care to avoid infections</li>
        </ul>
    </div>

    <div class="block">
        <h2>Health Care and Veterinary Visits</h2>
        <p>
            Routine veterinary care helps prevent disease and detect health issues early in all pets.
        </p>

        <ul>
            <li>Vaccinations and parasite prevention</li>
            <li>Regular health check-ups</li>
            <li>Weight and diet monitoring</li>
        </ul>

        <h3>Common Health Issues</h3>
        <ul>
            <li>Obesity due to overfeeding or lack of exercise</li>
            <li>Parasite infections (fleas, ticks, worms)</li>
            <li>Species-specific diseases (e.g., parvovirus in dogs, respiratory issues in small pets)</li>
        </ul>
    </div>

    <div class="block">
        <h2>Training and Behavior</h2>
        <p>
            Training helps pets learn good behavior and strengthens the bond with their owner.
            Positive reinforcement works best for dogs and can also be used for some small animals.
        </p>
        <p>
            Cats benefit from litter training and environmental enrichment.
        </p>
    </div>

    <div class="block">
        <h2>Emotional Well-being</h2>
        <p>
            Pets need attention, stimulation, and companionship.
            Lack of interaction can lead to stress, anxiety, or destructive behavior.
        </p>
    </div>

    <div class="block">
        <h2>Safety and Responsible Ownership</h2>
        <ul>
            <li>Keep pets in a safe and secure environment</li>
            <li>Use identification tags or microchips where possible</li>
            <li>Supervise pets outdoors</li>
            <li>Keep toxic foods and substances away</li>
        </ul>
    </div>

    <div class="block">
        <h2>When to Visit a Veterinarian</h2>
        <ul>
            <li>Loss of appetite or sudden weight changes</li>
            <li>Vomiting, diarrhea, or unusual behavior</li>
            <li>Difficulty breathing or moving</li>
            <li>Signs of pain, stress, or injury</li>
        </ul>
    </div>

