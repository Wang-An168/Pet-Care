    <div class="block">
        <h2>General Care for Small Pets</h2>
        <p>
            Small pets such as rabbits, hamsters, and guinea pigs need a safe, clean, and comfortable environment.
            Proper care includes the right diet, housing, and daily attention.
        </p>
    </div>

    <div class="block">
        <h2>Nutrition and Feeding</h2>
        <p>
            Each small pet has specific dietary needs. Fresh food, hay, and clean water are essential.
        </p>

        <ul>
            <li><strong>Rabbits:</strong> Unlimited hay, fresh vegetables, pellets</li>
            <li><strong>Hamsters:</strong> Seeds, grains, vegetables in small amounts</li>
            <li><strong>Guinea pigs:</strong> Hay, vitamin C-rich vegetables, pellets</li>
        </ul>
    </div>

    <div class="block">
        <h2>Housing and Environment</h2>
        <p>
            A proper cage or enclosure is important for safety and comfort.
        </p>

        <ul>
            <li>Spacious and well-ventilated cages</li>
            <li>Soft bedding material</li>
            <li>Regular cleaning to prevent disease</li>
            <li>Safe hiding spaces for stress reduction</li>
        </ul>
    </div>

    <div class="block">
        <h2>Exercise and Activity</h2>
        <p>
            Small pets need daily movement to stay healthy and happy.
        </p>

        <ul>
            <li>Rabbits: Free-roam time or play pens</li>
            <li>Hamsters: Running wheels and tunnels</li>
            <li>Guinea pigs: Floor time outside the cage</li>
        </ul>
    </div>

    <div class="block">
        <h2>Grooming and Hygiene</h2>
        <ul>
            <li>Regular brushing (especially for long-haired rabbits)</li>
            <li>Clean cages at least once a week</li>
            <li>Nail trimming when needed</li>
            <li>Check for parasites or skin issues</li>
        </ul>
    </div>

    <div class="block">
        <h2>Health Care and Veterinary Visits</h2>
        <p>
            Regular check-ups help prevent common health problems in small pets.
        </p>

        <ul>
            <li>Dental care (very important for rabbits and guinea pigs)</li>
            <li>Parasite prevention</li>
            <li>Monitoring weight and eating habits</li>
        </ul>

        <h3>Common Health Issues</h3>
        <ul>
            <li>Overgrown teeth (rabbits & guinea pigs)</li>
            <li>Respiratory infections</li>
            <li>Digestive problems</li>
            <li>Stress-related illnesses</li>
        </ul>
    </div>

    <div class="block">
        <h2>Behavior and Social Needs</h2>
        <p>
            Some small pets are social animals and should not be kept alone.
            Understanding their behavior improves their well-being.
        </p>

        <ul>
            <li>Rabbits: Prefer companionship of other rabbits</li>
            <li>Guinea pigs: Very social, should live in pairs</li>
            <li>Hamsters: Usually solitary animals</li>
        </ul>
    </div>

    <div class="block">
        <h2>Safety and Responsible Ownership</h2>
        <ul>
            <li>Protect from predators and household dangers</li>
            <li>Avoid toxic foods and plants</li>
            <li>Handle gently to avoid stress or injury</li>
            <li>Provide a quiet and stable environment</li>
        </ul>
    </div>

    <div class="block">
        <h2>When to Visit a Veterinarian</h2>
        <ul>
            <li>Loss of appetite or not eating</li>
            <li>Diarrhea or digestive issues</li>
            <li>Breathing problems</li>
            <li>Unusual behavior or lethargy</li>
        </ul>
    </div>

