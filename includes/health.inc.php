        <h3>Veterinary Care</h3>
        <p>
            Pets should have routine check-ups at least once a year. Puppies, kittens, senior pets, and animals with health
            conditions may need more frequent visits. Vets monitor weight, dental health, heart function, and overall condition.
        </p>
    </div>

    <div class="block">
        <h3>Vaccinations</h3>
        <p>
            Vaccinations protect pets from serious infectious diseases. Dogs are commonly vaccinated against rabies,
            distemper, and parvovirus, while cats are vaccinated against feline panleukopenia and respiratory diseases.
            Vaccination schedules depend on age, lifestyle, and risk level.
        </p>
    </div>

    <div class="block">
        <h3>Parasite Prevention</h3>
        <p>
            Fleas, ticks, and worms can cause serious health issues if untreated. Regular preventive treatments help protect
            pets from skin irritation, infections, anemia, and internal diseases.
        </p>
    </div>

    <div class="block">
        <h3>Monitoring Health at Home</h3>
        <ul>
            <li>Changes in appetite or drinking habits</li>
            <li>Lethargy or unusual tiredness</li>
            <li>Vomiting, diarrhea, or coughing</li>
            <li>Sudden weight loss or gain</li>
            <li>Changes in behavior or activity</li>
        </ul>
    </div>

    <div class="block">
        <h3>When to See a Vet Immediately</h3>
        <ul>
            <li>Difficulty breathing or severe weakness</li>
            <li>Persistent vomiting or diarrhea</li>
            <li>Signs of pain or injury</li>
            <li>Seizures or collapse</li>
        </ul>
    </div>

