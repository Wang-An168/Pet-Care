        <h3>Space and Enclosure</h3>
        <p>
            Rabbits should have enough space to move freely, stretch, and stand on their hind legs.
            A large indoor pen or rabbit-proofed room is often better than a small cage.
        </p>
    </div>

    <div class="block">
        <h3>Bedding and Cleanliness</h3>
        <p>
            Use soft, safe bedding such as hay or paper-based materials.
            The habitat should be cleaned regularly to prevent odor buildup and reduce the risk of infections.
        </p>
    </div>

    <div class="block">
        <h3>Hiding Spots and Comfort</h3>
        <p>
            Rabbits are prey animals and feel safest when they have hiding spaces.
            Provide boxes, tunnels, or covered areas where they can rest and feel secure.
        </p>
    </div>

    <div class="block">
        <h3>Safe Toys and Enrichment</h3>
        <p>
            Chew-safe toys, tunnels, and cardboard items help keep rabbits mentally stimulated and prevent boredom.
            Avoid plastic or toxic materials that could be harmful if chewed.
        </p>
    </div>

    <div class="block">
        <h3>Safety Tips</h3>
        <ul>
            <li>Keep electrical wires and toxic plants out of reach</li>
            <li>Ensure the enclosure is escape-proof</li>
            <li>Provide constant access to fresh hay and water</li>
            <li>Avoid loud noises and stressful environments</li>
        </ul>
    </div>

