        <h3>How often should I feed my pet?</h3>
        <p>
            Most adult dogs and cats should be fed 1–2 times per day. Puppies, kittens, and small pets may need
            more frequent meals depending on their age and dietary needs.
        </p>
    </div>

    <div class="faq-item">
        <h3>How often should I take my pet to the vet?</h3>
        <p>
            Pets should visit the vet at least once a year for a general check-up. Older pets or those with health
            issues may need more frequent visits.
        </p>
    </div>

    <div class="faq-item">
        <h3>Do all pets need vaccinations?</h3>
        <p>
            Yes, most pets require vaccinations to protect against serious diseases. The exact schedule depends on
            the type of animal, age, and local veterinary guidelines.
        </p>
    </div>

    <div class="faq-item">
        <h3>How much exercise does my pet need?</h3>
        <p>
            Dogs usually need daily walks and playtime, cats need interactive play sessions, and small pets need
            safe space to move and explore regularly.
        </p>
    </div>

    <div class="faq-item">
        <h3>How do I know if my pet is sick?</h3>
        <p>
            Common signs include loss of appetite, vomiting, diarrhea, tiredness, sudden behavior changes,
            or unusual weight loss. If you notice these signs, consult a veterinarian.
        </p>
    </div>

    <div class="faq-item">
        <h3>Can I feed my pet human food?</h3>
        <p>
            Some human foods are safe in small amounts, but many are harmful or toxic to pets. Always check
            with a vet before sharing human food with your pet.
        </p>
    </div>

