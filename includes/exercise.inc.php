        <h3>Dogs</h3>
        <p>
            Dogs need daily exercise such as walks, running, fetch, or agility games.
            The amount of exercise depends on breed, age, and energy level. Active breeds may need 1–2 hours daily,
            while smaller or older dogs may need shorter sessions.
        </p>
    </div>

    <div class="block">
        <h3>Cats</h3>
        <p>
            Cats benefit from short but frequent play sessions using toys, climbing structures, and interactive games.
            Exercise helps indoor cats prevent obesity and reduces boredom-related behavior problems.
        </p>
    </div>

    <div class="block">
        <h3>Small Pets</h3>
        <p>
            Small pets such as rabbits, guinea pigs, and hamsters need safe space to move and explore.
            Exercise wheels, tunnels, and supervised floor time help keep them active and healthy.
        </p>
    </div>

    <div class="block">
        <h3>Benefits of Regular Exercise</h3>
        <ul>
            <li>Maintains a healthy weight</li>
            <li>Improves heart and muscle strength</li>
            <li>Reduces anxiety and stress</li>
            <li>Prevents destructive behavior</li>
            <li>Supports mental stimulation and happiness</li>
        </ul>
    </div>

