        <h3>Basic Commands</h3>
        <p>
            Start with simple commands such as sit, stay, come, and leave it.
            Use short training sessions (5–10 minutes) to keep the puppy focused and avoid frustration.
        </p>
    </div>

    <div class="block">
        <h3>Positive Reinforcement</h3>
        <p>
            Reward good behavior with treats, praise, or toys. Avoid punishment, as it can lead to fear and confusion.
            Consistent rewards help puppies understand what is expected of them.
        </p>
    </div>

    <div class="block">
        <h3>House Training</h3>
        <p>
            Establish a regular schedule for feeding and bathroom breaks.
            Take your puppy outside frequently, especially after eating, drinking, or sleeping, to reinforce good habits.
        </p>
    </div>

    <div class="block">
        <h3>Socialization</h3>
        <p>
            Introduce your puppy to different people, environments, sounds, and other animals at an early age.
            Proper socialization helps prevent fear and aggression later in life.
        </p>
    </div>

    <div class="block">
        <h3>Consistency and Patience</h3>
        <p>
            Training takes time, and puppies learn best through repetition and routine.
            Stay consistent with rules and commands to avoid confusion.
        </p>
    </div>

