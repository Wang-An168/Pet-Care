    <div class="block">
        <h2>Nutrition and Feeding</h2>
        <p>
            Cats are obligate carnivores, which means they need a diet mainly based on animal protein.
            A balanced diet supports their energy, coat health, and immune system.
        </p>
        <p>
            Choose high-quality cat food based on age, weight, and health condition.
            Always provide fresh, clean water.
        </p>
    </div>

    <div class="block">
        <h2>Exercise and Play</h2>
        <p>
            Although cats are independent, they still need daily play and stimulation.
            Interactive toys and climbing structures help keep them active.
        </p>
        <p>
            Regular play prevents obesity and reduces stress-related behavior.
        </p>
    </div>

    <div class="block">
        <h2>Grooming and Hygiene</h2>
        <ul>
            <li>Regular brushing to reduce shedding and hairballs</li>
            <li>Clean litter box daily</li>
            <li>Nail trimming when needed</li>
            <li>Occasional bathing (only if necessary)</li>
            <li>Ear and eye checks for dirt or infection</li>
        </ul>
    </div>

    <div class="block">
        <h2>Health Care and Veterinary Visits</h2>
        <p>
            Regular vet check-ups help detect diseases early and ensure vaccinations are up to date.
        </p>

        <ul>
            <li>Vaccinations (e.g. feline distemper, rabies)</li>
            <li>Parasite prevention (fleas, worms)</li>
            <li>Routine health screenings</li>
        </ul>

        <h3>Common Health Issues</h3>
        <ul>
            <li>Urinary tract infections</li>
            <li>Obesity from overfeeding</li>
            <li>Dental disease</li>
            <li>Hairballs</li>
        </ul>
    </div>

    <div class="block">
        <h2>Behavior and Communication</h2>
        <p>
            Cats communicate through body language, vocal sounds, and behavior.
            Understanding these signals helps build a stronger bond.
        </p>
        <p>
            Scratching, kneading, and purring are natural behaviors, not misbehavior.
        </p>
    </div>

    <div class="block">
        <h2>Emotional Well-being</h2>
        <p>
            Cats need a safe environment, routine, and mental stimulation.
            Stress can lead to behavioral or health problems.
        </p>
    </div>

    <div class="block">
        <h2>Safety and Responsible Ownership</h2>
        <ul>
            <li>Keep toxic plants and foods out of reach</li>
            <li>Provide safe indoor/outdoor environments</li>
            <li>Use identification (collar or microchip)</li>
            <li>Regularly check for injuries or illness</li>
        </ul>
    </div>

    <div class="block">
        <h2>When to Visit a Veterinarian</h2>
        <ul>
            <li>Loss of appetite or sudden weight changes</li>
            <li>Vomiting or diarrhea lasting more than a day</li>
            <li>Difficulty urinating</li>
            <li>Behavior changes or signs of pain</li>
        </ul>
    </div>

