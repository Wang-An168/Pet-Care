    <div class="block">
        <h3>Bathing</h3>
        <p>
            Dogs should typically be bathed every 3–4 weeks, depending on breed, coat type, and activity level.
            Cats usually do not require regular baths as they groom themselves, unless they are dirty or have a medical need.
            Small pets like rabbits and guinea pigs generally do not need bathing and should be spot-cleaned instead.
        </p>
    </div>

    <div class="block">
        <h3>Coat Care</h3>
        <p>
            Regular brushing removes loose hair, prevents matting, and helps distribute natural skin oils.
            Long-haired breeds require daily brushing, while short-haired pets may only need weekly grooming.
        </p>
    </div>

    <div class="block">
        <h3>Ear, Eye, and Dental Care</h3>
        <ul>
            <li><strong>Ears:</strong> Check weekly for dirt, redness, or bad odor that may indicate infection.</li>
            <li><strong>Eyes:</strong> Clean gently if discharge appears, using a vet-approved solution.</li>
            <li><strong>Teeth:</strong> Brush regularly to prevent plaque buildup, gum disease, and bad breath.</li>
        </ul>
    </div>

    <div class="block">
        <h3>Nail Care</h3>
        <p>
            Nails should be trimmed regularly to prevent overgrowth, which can cause pain or walking problems.
            Active dogs may naturally wear nails down, but indoor pets usually need more frequent trimming.
        </p>
    </div>

    <div class="block">
        <h3>Living Environment</h3>
        <p>
            Clean bedding, litter boxes, cages, and food bowls regularly to prevent bacteria and parasite buildup.
            A clean environment reduces the risk of infections and improves overall pet comfort and health.
        </p>
    </div>

